<?php

namespace AppBundle\Entity;

/**
 * Class Room
 * @package AppBundle\Entity
 */
class Room
{
    /** @var integer */
    private $id;
    /** @var string */
    private $name;
    /** @var string */
    private $key;

    /**
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * @return string
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * @param string $name
     */
    public function setName($name)
    {
        $this->name = $name;
    }

    /**
     * @return string
     */
    public function getKey()
    {
        return $this->key;
    }

    /**
     * @param string $key
     */
    public function setKey($key)
    {
        $this->key = $key;
    }
}
